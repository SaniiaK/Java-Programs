package com.java.class04;

public class IfElse {

    public static void main(String[] args) { // what is the purpose of if/else - it can execute one of the conditions

        int a = 11;
        boolean b = a >= 11;

        if (b) {
            System.out.println("1");
        } else {
            System.out.println("2");
        }
        System.out.println("4");
    }
}